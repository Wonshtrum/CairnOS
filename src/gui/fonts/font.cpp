uint8_t* Font::get(char letter) {
	if ('A' <= letter && letter <= 'Z') {
		return stencils + (letter-'A')*size;
	}
	if ('a' <= letter && letter <= 'z') {
		return stencils + (letter-'a')*size;
	}
	return 0;
}


