Font Fonts::get(uint8_t width, uint8_t height) {
	if (width == 4 && height == 4) { return { Font4x4, width, height, 2 }; }
	else if (width == 3 && height == 5) { return { Font3x5, width, height, 2 }; }
	else if (width == 5 && height == 5) { return { Font5x5, width, height, 4 }; }

	return { FUnknown, width, height, 0 };
}
